// MARK: - Performance Tests

class ADIFPerformanceTests: XCTestCase {

    func testParse10kRecords() {
        var adif = "<ADIF_VER:5>3.1.4\n<EOH>\n"
        for i in 0..<10_000 {
            let call = String(format: "W%04dAA", i)
            let date = "20240315"
            let time = String(format: "%02d%02d00", (i / 60) % 24, i % 60)
            adif += "<CALL:\(call.count)>\(call) <BAND:3>20m <MODE:3>FT8 "
            adif += "<QSO_DATE:8>\(date) <TIME_ON:6>\(time) "
            adif += "<RST_SENT:3>-10 <RST_RCVD:3>-08 "
            adif += "<GRIDSQUARE:4>FN31 <TX_PWR:2>50 <EOR>\n"
        }

        let start = ContinuousClock.now
        let result = ADIFParser.parse(string: adif)
        let elapsed = ContinuousClock.now - start

        XCTAssertEqual(result.records.count, 10_000)
        XCTAssertNotNil(result.header)
        XCTAssertTrue(result.errors.isEmpty)
        XCTAssertTrue(elapsed < .seconds(10), "Parsing 10k records took \(elapsed)")
    }

    func testExport10kRecords() {
        var records: [ADIFRecord] = []
        for i in 0..<10_000 {
            var record = ADIFRecord()
            record.setField(ADIFField(name: "CALL", value: String(format: "W%04dAA", i)))
            record.setField(ADIFField(name: "BAND", value: "20m"))
            record.setField(ADIFField(name: "MODE", value: "FT8"))
            record.setField(ADIFField(name: "QSO_DATE", value: "20240315"))
            record.setField(ADIFField(name: "TIME_ON", value: String(format: "%02d%02d00", (i / 60) % 24, i % 60)))
            records.append(record)
        }

        let start = ContinuousClock.now
        let output = ADIFExporter.export(records: records, options: .init(lineEnding: "\n"))
        let elapsed = ContinuousClock.now - start

        XCTAssertTrue(output.count > 0)
        XCTAssertTrue(elapsed < .seconds(5), "Exporting 10k records took \(elapsed)")
    }

    func testRoundTrip1kRecords() {
        var adif = "<ADIF_VER:5>3.1.4\n<EOH>\n"
        for i in 0..<1_000 {
            let call = String(format: "K%03dAB", i)
            adif += "<CALL:\(call.count)>\(call) <BAND:3>20m <MODE:2>CW "
            adif += "<QSO_DATE:8>20240315 <TIME_ON:6>120000 <RST_SENT:3>599 <RST_RCVD:3>579 "
            adif += "<GRIDSQUARE:6>FN31ab <TX_PWR:3>100 <COMMENT:4>test <EOR>\n"
        }

        let first = ADIFParser.parse(string: adif)
        XCTAssertEqual(first.records.count, 1_000)

        let exported = ADIFExporter.export(records: first.records, options: .init(includeHeader: false, lineEnding: "\n"))
        let second = ADIFParser.parse(string: exported)
        XCTAssertEqual(second.records.count, 1_000)

        XCTAssertEqual(first.records[0]["CALL"], second.records[0]["CALL"])
        XCTAssertEqual(first.records[500]["CALL"], second.records[500]["CALL"])
        XCTAssertEqual(first.records[999]["CALL"], second.records[999]["CALL"])
    }
}

