// MARK: - Edge Cases

class ADIFEdgeCaseTests: XCTestCase {

    func testEmptyFile() {
        let result = ADIFParser.parse(string: "")
        XCTAssertNil(result.header)
        XCTAssertTrue(result.records.isEmpty)
        XCTAssertTrue(result.errors.isEmpty)
    }

    func testHeaderOnly() {
        let adif = "<ADIF_VER:5>3.1.4\n<PROGRAMID:7>TestApp\n<EOH>\n"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.header?["ADIF_VER"], "3.1.4")
        XCTAssertTrue(result.records.isEmpty)
    }

    func testNoHeader() {
        let adif = "<CALL:4>W1AW <BAND:3>20m <EOR>\n<CALL:5>VE3AB <BAND:3>40m <EOR>\n"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNil(result.header)
        XCTAssertEqual(result.records.count, 2)
    }

    func testWindowsCRLF() {
        let adif = "<ADIF_VER:5>3.1.4\r\n<EOH>\r\n<CALL:4>W1AW <BAND:3>20m <EOR>\r\n"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
    }

    func testUnixLF() {
        let adif = "<ADIF_VER:5>3.1.4\n<EOH>\n<CALL:4>W1AW <BAND:3>20m <EOR>\n"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.records.count, 1)
    }

    func testClassicMacCR() {
        let adif = "<ADIF_VER:5>3.1.4\r<EOH>\r<CALL:4>W1AW <BAND:3>20m <EOR>\r"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.records.count, 1)
    }

    func testPreambleText() {
        let adif = """
        This is a preamble comment that appears before any tags.
        It can span multiple lines. Only text after '<' is parsed.
        <CALL:4>W1AW <BAND:3>20m <EOR>
        """
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
    }

    func testMultipleWhitespaceBetweenFields() {
        let adif = "<CALL:4>W1AW   \n\n   <BAND:3>20m\n\n\n<MODE:3>FT8   <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[0]["BAND"], "20m")
        XCTAssertEqual(result.records[0]["MODE"], "FT8")
    }

    func testMinimalRecord() {
        let adif = "<CALL:4>W1AW <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[0].fieldCount, 1)
    }

    func testVeryLargeFieldValue() {
        let longComment = String(repeating: "A", count: 1500)
        let adif = "<CALL:4>W1AW <COMMENT:\(longComment.count)>\(longComment) <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["COMMENT"], longComment)
    }

    func testUnicodeFieldValues() {
        let name = "Hans Müller"
        let adif = "<CALL:5>DL1AB <NAME:\(name.count)>\(name) <COMMENT:7>73 & ü <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["NAME"], name)
    }

    func testEmptyRecordSkipped() {
        let adif = "<CALL:4>W1AW <EOR><EOR><CALL:5>VE3AB <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 2)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[1]["CALL"], "VE3AB")
    }

    func testFieldWithNewlinesInValue() {
        let adif = "<CALL:4>W1AW <COMMENT:12>Line1\nLine2\n <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        let comment = result.records[0]["COMMENT"]
        XCTAssertEqual(comment?.contains("\n"), true)
    }

    func testEohCaseInsensitive() {
        let adif = "<ADIF_VER:5>3.1.4\n<eoh>\n<CALL:4>W1AW <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.records.count, 1)
    }

    func testEorCaseInsensitive() {
        let adif = "<CALL:4>W1AW <eor>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
    }

    func testMixedCaseEOHEOR() {
        let adif = "<ADIF_VER:5>3.1.4\n<Eoh>\n<CALL:4>W1AW <Eor>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.records.count, 1)
    }

    func testWhitespaceOnlyFile() {
        let result = ADIFParser.parse(string: "   \n\n  \t  \n")
        XCTAssertTrue(result.records.isEmpty)
        XCTAssertNil(result.header)
    }

    func testOnlyPreambleNoTags() {
        let result = ADIFParser.parse(string: "This is just text with no ADIF tags whatsoever.")
        XCTAssertTrue(result.records.isEmpty)
        XCTAssertNil(result.header)
    }

