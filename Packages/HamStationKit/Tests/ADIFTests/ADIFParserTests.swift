// MARK: - Basic Parsing

class ADIFBasicParsingTests: XCTestCase {

    func testParseSingleQSO() {
        let adif = "<CALL:4>W1AW <BAND:3>20m <MODE:3>FT8 <QSO_DATE:8>20240315 <TIME_ON:6>143022 <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[0]["BAND"], "20m")
        XCTAssertEqual(result.records[0]["MODE"], "FT8")
        XCTAssertEqual(result.records[0]["QSO_DATE"], "20240315")
        XCTAssertEqual(result.records[0]["TIME_ON"], "143022")
        XCTAssertNil(result.header)
        XCTAssertTrue(result.errors.isEmpty)
    }

    func testParseAllCommonFields() {
        let adif = """
        <CALL:4>W1AW <BAND:3>20m <MODE:3>FT8 <FREQ:8>14.07400 \
        <QSO_DATE:8>20240315 <TIME_ON:6>143022 <TIME_OFF:6>143052 \
        <RST_SENT:3>-10 <RST_RCVD:3>-08 <TX_PWR:2>50 \
        <GRIDSQUARE:4>FN31 <NAME:8>John Doe <QTH:9>Newington \
        <COMMENT:11>Good signal <COUNTRY:3>USA <CQZ:1>5 <ITUZ:1>8 \
        <DXCC:3>291 <CONT:2>NA <EOR>
        """
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        let r = result.records[0]
        XCTAssertEqual(r["CALL"], "W1AW")
        XCTAssertEqual(r["FREQ"], "14.07400")
        XCTAssertEqual(r["TX_PWR"], "50")
        XCTAssertEqual(r["GRIDSQUARE"], "FN31")
        XCTAssertEqual(r["NAME"], "John Doe")
        XCTAssertEqual(r["QTH"], "Newington")
        XCTAssertEqual(r["COMMENT"], "Good signal")
        XCTAssertEqual(r["COUNTRY"], "USA")
        XCTAssertEqual(r["CQZ"], "5")
        XCTAssertEqual(r["ITUZ"], "8")
        XCTAssertEqual(r["DXCC"], "291")
        XCTAssertEqual(r["CONT"], "NA")
        XCTAssertEqual(r["TIME_OFF"], "143052")
    }

    func testParseHeaderAndMultipleRecords() {
        let adif = """
        Generated by TestApp
        <ADIF_VER:5>3.1.4
        <PROGRAMID:7>TestApp
        <PROGRAMVERSION:3>1.0
        <EOH>
        <CALL:4>W1AW <BAND:3>20m <MODE:3>FT8 <QSO_DATE:8>20240315 <TIME_ON:6>143022 <EOR>
        <CALL:5>VE3AB <BAND:3>40m <MODE:2>CW <QSO_DATE:8>20240315 <TIME_ON:6>150000 <EOR>
        <CALL:5>G3XYZ <BAND:3>20m <MODE:3>SSB <QSO_DATE:8>20240316 <TIME_ON:6>081500 <EOR>
        """
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.header?["ADIF_VER"], "3.1.4")
        XCTAssertEqual(result.header?["PROGRAMID"], "TestApp")
        XCTAssertEqual(result.header?["PROGRAMVERSION"], "1.0")
        XCTAssertEqual(result.records.count, 3)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[1]["CALL"], "VE3AB")
        XCTAssertEqual(result.records[2]["CALL"], "G3XYZ")
    }

    func testParseHeaderFields() {
        let adif = "<ADIF_VER:5>3.1.4\n<PROGRAMID:6>WSJT-X\n<PROGRAMVERSION:5>2.7.0\n<EOH>\n"
        let result = ADIFParser.parse(string: adif)
        XCTAssertNotNil(result.header)
        XCTAssertEqual(result.header?["ADIF_VER"], "3.1.4")
        XCTAssertEqual(result.header?["PROGRAMID"], "WSJT-X")
        XCTAssertEqual(result.header?["PROGRAMVERSION"], "2.7.0")
        XCTAssertTrue(result.records.isEmpty)
    }

    func testParseTwoRecordsBackToBack() {
        let adif = "<CALL:4>W1AW <EOR><CALL:5>VE3AB <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 2)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[1]["CALL"], "VE3AB")
    }

    func testParseSubMode() {
        let adif = "<CALL:4>W1AW <MODE:3>FT8 <SUBMODE:4>FT8+ <BAND:3>20m <QSO_DATE:8>20240315 <TIME_ON:6>143022 <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records[0]["SUBMODE"], "FT8+")
    }
}

// MARK: - Field Handling

class ADIFFieldHandlingTests: XCTestCase {

    func testCaseInsensitiveFieldNames() {
        let adif = "<call:4>W1AW <Band:3>20m <MODE:3>FT8 <Qso_Date:8>20240315 <time_on:6>143022 <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        XCTAssertEqual(result.records[0]["call"], "W1AW")
        XCTAssertEqual(result.records[0]["Call"], "W1AW")
        XCTAssertEqual(result.records[0]["BAND"], "20m")
        XCTAssertEqual(result.records[0]["QSO_DATE"], "20240315")
    }

    func testFieldWithTypeIndicator() {
        let adif = "<CALL:4:S>W1AW <QSO_DATE:8:D>20240315 <FREQ:8:N>14.07400 <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1AW")
        let callField = result.records[0].field(named: "CALL")
        XCTAssertEqual(callField?.typeIndicator, "S")
        let dateField = result.records[0].field(named: "QSO_DATE")
        XCTAssertEqual(dateField?.typeIndicator, "D")
        let freqField = result.records[0].field(named: "FREQ")
        XCTAssertEqual(freqField?.typeIndicator, "N")
    }

    func testFieldLengthShorterThanData() {
        let adif = "<CALL:3>W1AW <BAND:3>20m <EOR>"
        let result = ADIFParser.parse(string: adif, mode: .lenient)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "W1A")
    }

    func testFieldLengthLongerThanData() {
        let adif = "<CALL:10>W1AW"
        let result = ADIFParser.parse(string: adif, mode: .lenient)
        XCTAssertTrue(result.records.isEmpty || result.records[0]["CALL"] == "W1AW")
    }

    func testAppFieldsPreserved() {
        let adif = "<CALL:4>W1AW <APP_HAMRD_QSO_ID:5>12345 <APP_N1MM_POINTS:1>2 <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["APP_HAMRD_QSO_ID"], "12345")
        XCTAssertEqual(result.records[0]["APP_N1MM_POINTS"], "2")
        XCTAssertTrue(result.records[0].hasField("APP_HAMRD_QSO_ID"))
        XCTAssertTrue(result.records[0].hasField("APP_N1MM_POINTS"))
    }

    func testUnknownFieldsPreserved() {
        let adif = "<CALL:4>W1AW <TOTALLY_UNKNOWN_FIELD:5>hello <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["TOTALLY_UNKNOWN_FIELD"], "hello")
    }

    func testEmptyFieldValue() {
        let adif = "<CALL:4>W1AW <COMMENT:0> <BAND:3>20m <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["COMMENT"], "")
        XCTAssertTrue(result.records[0].hasField("COMMENT"))
    }

    func testFieldNameUppercase() {
        let field = ADIFField(name: "call", value: "W1AW")
        XCTAssertEqual(field.name, "CALL")
    }

    func testFieldEquality() {
        let a = ADIFField(name: "CALL", value: "W1AW", typeIndicator: "S")
        let b = ADIFField(name: "call", value: "W1AW", typeIndicator: "S")
        XCTAssertEqual(a, b)
    }

    func testFieldHashing() {
        let a = ADIFField(name: "CALL", value: "W1AW")
        let b = ADIFField(name: "call", value: "W1AW")
        var set: Set<ADIFField> = [a]
        set.insert(b)
        XCTAssertEqual(set.count, 1)
    }

    func testRecordSubscriptCaseInsensitive() {
        var record = ADIFRecord()
        record.setField(name: "CALL", value: "W1AW")
        XCTAssertEqual(record["call"], "W1AW")
        XCTAssertEqual(record["Call"], "W1AW")
        XCTAssertEqual(record["CALL"], "W1AW")
    }

    func testRecordHasFieldCaseInsensitive() {
        var record = ADIFRecord()
        record.setField(name: "CALL", value: "W1AW")
        XCTAssertTrue(record.hasField("call"))
        XCTAssertTrue(record.hasField("CALL"))
        XCTAssertFalse(record.hasField("BAND"))
    }

    func testRecordAllFieldNamesSorted() {
        let fields = [
            ADIFField(name: "MODE", value: "FT8"),
            ADIFField(name: "CALL", value: "W1AW"),
            ADIFField(name: "BAND", value: "20m"),
        ]
        let record = ADIFRecord(fields: fields)
        XCTAssertEqual(record.allFieldNames, ["BAND", "CALL", "MODE"])
    }

    func testRecordSetAndRemove() {
        var record = ADIFRecord()
        record.setField(name: "CALL", value: "W1AW")
        XCTAssertEqual(record.fieldCount, 1)
        record.removeField(named: "call")
        XCTAssertTrue(record.isEmpty)
    }

    func testRecordDescription() {
        var record = ADIFRecord()
        record.setField(name: "CALL", value: "W1AW")
        let desc = record.description
        XCTAssertTrue(desc.contains("<CALL:4>W1AW"))
        XCTAssertTrue(desc.contains("<EOR>"))
    }

    func testFieldDescriptionWithType() {
        let field = ADIFField(name: "CALL", value: "W1AW", typeIndicator: "S")
        XCTAssertEqual(field.description, "<CALL:4:S>W1AW")
    }

    func testFieldDescriptionWithoutType() {
        let field = ADIFField(name: "CALL", value: "W1AW")
        XCTAssertEqual(field.description, "<CALL:4>W1AW")
    }

    func testDuplicateFieldNamesLastWins() {
        let adif = "<CALL:4>W1AW <CALL:5>VE3AB <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records.count, 1)
        XCTAssertEqual(result.records[0]["CALL"], "VE3AB")
    }

    func testMultipleAppFieldsSameProgram() {
        let adif = "<CALL:4>W1AW <APP_HAMRD_QSO_ID:5>12345 <APP_HAMRD_COLOR:3>red <APP_HAMRD_FLAG:4>true <EOR>"
        let result = ADIFParser.parse(string: adif)
        XCTAssertEqual(result.records[0]["APP_HAMRD_QSO_ID"], "12345")
        XCTAssertEqual(result.records[0]["APP_HAMRD_COLOR"], "red")
        XCTAssertEqual(result.records[0]["APP_HAMRD_FLAG"], "true")
    }
}

